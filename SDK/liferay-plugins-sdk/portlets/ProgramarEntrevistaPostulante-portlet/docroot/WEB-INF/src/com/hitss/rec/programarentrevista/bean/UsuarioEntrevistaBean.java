package com.hitss.rec.programarentrevista.bean;

public class UsuarioEntrevistaBean {

	private long userId;
	private String fullname;
	private String fechaPostulancion;
	private int disponibilidad;
	private String interno;
	private String fase;
	private String dni;
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public String getFechaPostulancion() {
		return fechaPostulancion;
	}
	public void setFechaPostulancion(String fechaPostulancion) {
		this.fechaPostulancion = fechaPostulancion;
	}
	public int getDisponibilidad() {
		return disponibilidad;
	}
	public void setDisponibilidad(int disponibilidad) {
		this.disponibilidad = disponibilidad;
	}
	public String getInterno() {
		return interno;
	}
	public void setInterno(String interno) {
		this.interno = interno;
	}
	public String getFase() {
		return fase;
	}
	public void setFase(String fase) {
		this.fase = fase;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	
	
	
	
	
}
