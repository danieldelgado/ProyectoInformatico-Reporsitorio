package com.hitss.rev.bean;
public class Haming {
	Double valorDefinido;
	Double valor;

	public Haming() {
		
	}
	public Haming(Double valorDefinido, Double valor) {
		super();
		this.valorDefinido = valorDefinido;
		this.valor = valor;
	}

	public Double getValorDefinido() {
		return valorDefinido;
	}

	public void setValorDefinido(Double valorDefinido) {
		this.valorDefinido = valorDefinido;
	}

	public Double getValor() {
		return valor;
	}

	public void setValor(Double valor) {
		this.valor = valor;
	}

}